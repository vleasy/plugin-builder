export function getItemLabel(value: string): string {
  const found = ALL_ITEMS.find(i => i.value === value)
  return found ? found.label : value
}

export function getItemCategory(value: string): string {
  const found = ALL_ITEMS.find(i => i.value === value)
  return found ? found.category : 'unknown'
}

export function searchItems(query: string, maxResults = 50): ItemOption[] {
  const q = query.toLowerCase()
  if (!q) return ALL_ITEMS.slice(0, maxResults)

  const results = ALL_ITEMS.filter(i =>
    i.value.toLowerCase().includes(q) ||
    i.label.toLowerCase().includes(q)
  )
  results.sort((a, b) => {
    const aExact = a.value.toLowerCase() === q || a.label.toLowerCase() === q ? -1 : 0
    const bExact = b.value.toLowerCase() === q || b.label.toLowerCase() === q ? 1 : 0
    if (aExact || bExact) return aExact + bExact

    const aStarts = a.value.toLowerCase().startsWith(q) ? -1 : 0
    const bStarts = b.value.toLowerCase().startsWith(q) ? 1 : 0
    if (aStarts || bStarts) return aStarts + bStarts

    return compareCat(a.category, b.category)
  })
  return results.slice(0, maxResults)
}

